package ders13_nestedForLoop_method0lusturma;

public class C03_NesedForLoop {

    public static void main(String[] args) {

        //1-4 arasindaki sayilar icin carpim taplosu olusturalim.

        for (int i = 1; i <=5 ; i++) {

            for (int j = 1 ; j <=5 ; j++) {
                System.out.print(i*j + " ");
            }
            System.out.println("");
        }
    }
}

